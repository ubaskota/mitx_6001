def getWordScore(word, n):
    score = 0
    count_n = 0
    
    for i in word:
        score = score + SCRABBLE_LETTER_VALUES[i]
        count_n = count_n + 1
        
    if (count_n < n):
        return score * len(word)
    else:
        return score * len(word) + 50
        

